package server.serv;

public class AudioData {
    public String name, audio, lenAudio, part, by;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAudio() {
        return audio;
    }

    public void setAudio(String audio) {
        this.audio = audio;
    }

    public String getLenAudio() {
        return lenAudio;
    }

    public void setLenAudio(String lenAudio) {
        this.lenAudio = lenAudio;
    }

    public String getPart() {
        return part;
    }

    public void setPart(String part) {
        this.part = part;
    }

    public String getBy() {
        return by;
    }

    public void setBy(String by) {
        this.by = by;
    }
}
